# Capitalize just the first letter of string
puts "hello"
puts "hello".capitalize()
puts "heLLo".capitalize()
puts "hello world".capitalize()
puts

puts "hello".capitalize().class()
puts "heLLo".capitalize().class()
puts "hello world".capitalize().class()
puts

# Change the case of the whole string
puts "hello".upcase()
puts "HELLO".downcase()
puts

#Reverse the case of all characters
puts "Paul".swapcase()
