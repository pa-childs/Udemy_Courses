# Use methods on string objects
puts "Hello World".length
puts "Hello World".upcase
puts "Hello World".downcase
puts

# Use methods on a string variable
my_string = "Hello World"
puts my_string.length
puts my_string.upcase
puts

# Use methods on integer object
puts 10.next
puts -1.next
puts

# Use of .inspect is same as p method
puts "Hello World"
puts "Hello World".inspect
p "Hello World"
