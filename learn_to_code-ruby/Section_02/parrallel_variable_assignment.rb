# Parrallel variable assignment
number_one = 10
number_two = 20
number_three = 30

puts number_one, number_two, number_three

number_one, number_two , number_three = 15, 25, 35

puts number_one, number_two, number_three

# Swapping values of variables
value_one = 1
value_two = 2

puts value_one, value_two

value_one, value_two = value_two, value_one

puts value_one, value_two
